$(function(){
	$payment_div2_div1=$(".payment_div2_div1")
	$payment_div2_div1_img1=$(".payment_div2_div1_img1")
	$payment_div2_div1_img2=$(".payment_div2_div1_img2")
	$payment_div2_div2=$(".payment_div2_div2")
	$payment_div2_div2_img1=$(".payment_div2_div2_img1")
	$payment_div2_div2_img2=$(".payment_div2_div2_img2")
	$payment_div2_a1=$(".payment_div2_a1")
	$payment_div2_div1.click(function(){
		$payment_div2_div1_img1.css({"display":"none"})
		$payment_div2_div1_img2.css({"display":"block"})
		$payment_div2_div2_img1.css({"display":"block"})
		$payment_div2_div2_img2.css({"display":"none"})
		$payment_div2_a1.html("使用支付宝付款")
		$("#payType").val("1")
	})
	$payment_div2_div2.click(function(){
		$payment_div2_div1_img1.css({"display":"block"})
		$payment_div2_div1_img2.css({"display":"none"})
		$payment_div2_div2_img1.css({"display":"none"})
		$payment_div2_div2_img2.css({"display":"block"})
		$payment_div2_a1.html("使用微信付款")
		$("#payType").val("2")
	})
})
